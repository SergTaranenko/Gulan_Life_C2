# Быстрый словарь по id
FLAVORS_BY_ID = {f[0]: f for f in ICE_CREAM_FLAVORS}

def get_available_flavors(rank_index: int) -> list:
    """Возвращает вкусы, доступные на текущем ранге"""
    return [f for f in ICE_CREAM_FLAVORS if f[3] <= rank_index]

def get_random_flavor(rank_index: int) -> tuple | None:
    """Возвращает случайный доступный вкус"""
    available = get_available_flavors(rank_index)
    return random.choice(available) if available else None

def get_cipher_text(rank_index: int) -> str:
    """Полная расшифровка Ледника Шамана для текущего ранга"""
    available = get_available_flavors(rank_index)
    locked = [f for f in ICE_CREAM_FLAVORS if f[3] > rank_index]

    lines = ["🧊 ЛЕДНИК ШАМАНА — СЕКРЕТНЫЙ РЕЕСТР\n"]
    lines.append("Доступные вкусы:\n")
    for fid, name, meaning, _ in available:
        lines.append(f"{fid}. {name}\n   → {meaning}\n")

    if locked:
        lines.append(f"\n🔒 Закрытых вкусов: {len(locked)}")
        lines.append("Откроются с повышением ранга.")

    return "\n".join(lines)

def get_flavor_display_name(fid: int) -> str:
    """Только название без расшифровки — для основного бота"""
    f = FLAVORS_BY_ID.get(fid)
    return f[1] if f else "🍦 Мороженое"

